<h5>Voici un petit tutoriel pour mettre un site en ligne sur FileZila.</h5>
<br>
<iframe width="560" height="315" src="https://www.youtube.com/embed/3gAeASon6ys" title="YouTube video player" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share" allowfullscreen></iframe>
<p>Comment mettre en ligne son site web par FTP ?<p>
<p>Pour publier votre Site Web en ligne via le mode FTP,
Commencez par télécharger un bon logiciel FTP gratuit : <br>


Pour télécharger FILEZILLA cliquez le lien ci-bas: <a href="https://filezilla-project.org/download.php?type=client">lien disponible ici.</a></p>

<img src="https://zupimages.net/up/23/12/ugaa.jpg" alt="filezila" width="400px" height="400px"> 

<p>Cliquez ensuite sur « CONNECTER »
<br>

Une fois connecté : <br>
Les carreaux en bas à gauche, <br>
ce sont vos fichiers de votre ordinateur <br>

<br>
Les carreaux en bas à droite,
ce sont les fichers de votre compte hébergement web sur serveur. Vous devez cliquer a droite sur le repertoire « domains » ensuite entrez dans le répetoire de votre nom de domaine, ensuite déposez tout vos fichiers dans le répertoire public_html. Vous y verrez 2 ou 3 répertoires et un fichier index.html. Ne pas toucher et mettre tout votre site web dans ce carreau en utilisant la flèche de droite. ( les fichiers de votre site web doivent être tous dans le public_html )

<br>
N’oubliez pas :
La première page vu de votre site web doit ce nommer index.html, index.htm ou index.php
<br>

Quand votre compte est un nouveau forfait d'hébergement web,
il se trouve déjà un fichier index.html dans votre répertoire public_html. 
<br>
Donc ne pas oublier de le supprimer avant de publier votre site sur le FTP.</p>

